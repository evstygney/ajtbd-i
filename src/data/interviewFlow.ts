export function createSteps(mode: InterviewMode): StepState[] {
  const templates = getStepTemplates(mode);
  return templates.map((template, index) => ({
    id: template.id,
    phase: template.phase,
    title: template.title,
    status: index === 0 ? "active" : "todo",
    notes: "",
  }));
}

export function getStepTemplate(stepId: string, mode: InterviewMode): StepTemplate {
  const template = getStepTemplates(mode).find((item) => item.id === stepId);
  if (!template) {
    throw new Error(`Unknown step template: ${stepId}`);
  }
  return template;
}

export function getSuggestedJobTitle(job?: JobNode): string {
  if (!job) {
    return "Выберите или создайте работу";
  }

  return job.fields.expectedOutcome || job.title;
}
